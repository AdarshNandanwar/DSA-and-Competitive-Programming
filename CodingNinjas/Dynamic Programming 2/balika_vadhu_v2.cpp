int solve(string s1, string s2, int l1, int l2, int length, int k, int *** dp){
    if(l1 == 0 || l2 == 0){
        if(length != k){
            return INT_MIN;
        } else {
            return 0;
        }
    }
    if(length > k){
        return INT_MIN;
    }
    if(dp[l1][l2][length] != -1){
        return dp[l1][l2][length];
    }
    int ans;
    if(s1[0] == s2[0]){
        int option1 = int(s1[0]) + solve(s1.substr(1), s2.substr(1), l1-1, l2-1, length+1, k, dp);
        int option2 = solve(s1.substr(1), s2.substr(1), l1-1, l2-1, length, k, dp);
        ans = max(option1, option2);
    } else {
        int option1 = solve(s1.substr(1), s2, l1-1, l2, length, k, dp);
        int option2 = solve(s1, s2.substr(1), l1, l2-1, length, k, dp);
        ans = max(option1, option2);
    }
    dp[l1][l2][length] = ans;
    return ans;
}

